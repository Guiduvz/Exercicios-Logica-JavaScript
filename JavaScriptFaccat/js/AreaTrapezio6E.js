alert("Área do trapézio");
let baseMenor = parseInt(prompt("Digite o valor da base menor do trapézio: "))
let baseMaior = parseInt(prompt("Digite o valor da maior do trapézio: "))
let altura = parseInt(prompt("Digite o valor da altura do trapézio: "))
let resultado = ((baseMaior+baseMenor) * altura) / 2
alert("A área do trapézio é: "+ resultado +"cm²")